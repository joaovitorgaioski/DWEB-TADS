function somar(n1, n2) {
    var r = n1 + n2
    alert("O resultado da soma é:\n" + r)
}

function subtrair(n1, n2) {
    var r = n1 - n2
    alert("O resultado da subtração é:\n" + r)
}

function multiplicar(n1, n2) {
    var r = n1 * n2
    return(r)
}